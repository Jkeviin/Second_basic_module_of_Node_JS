const os = require('os');

console.log(os.homedir());  // Lo que hace es devolver el directorio home del usuario
console.log(os.hostname());  // Devuelve el nombre de la máquina
console.log(os.type());  // Devuelve el tipo de sistema operativo
console.log(os.release());  // Devuelve la versión del sistema operativo
console.log(os.uptime());  // Devuelve el tiempo que lleva encendido el sistema operativo
